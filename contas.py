print("Algorítmo de operações aritméticas")

#Digitando os valores

valor1 = input("Digite o primeiro valor: ")
valor2 = input("Digite o segundo valor: ")

#Realizando as operações

#Soma
soma = int(valor1) + int(valor2)

#Subtração
sub = int(valor1) - int(valor2)

#Multiplicação
multi = int(valor1) * int(valor2)

#Divisão
div = float(valor1) / float(valor2)

print("O resultado da soma é: {}".format(soma))
print("O resultado da subtração é: {}".format(sub))
print("O resultado da multiplicação é: {}".format(multi))
print("O resultado da divisão é: {}".format(div))